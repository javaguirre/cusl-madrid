<h1>Colabora con el V Concurso Universitario de Software Libre</h1>
<div class="node">
  <div class="content"><p>La organización del <strong>Concurso de Software Libre</strong>
    está formada por estudiantes y docentes universitarios. Necesitamos tu colaboración,
    la de tu grupo de usuarios/as o la de la Oficina de Software Libre de tu Universidad.</p>

    <h2>Colabora</h2>

    <ul>
      <li>Queremos sumarnos a la demás iniciativa que en tu Universidad se están realizando a favor del Software Libre.</li>
      <li>Necesitamos <strong>difusión en tu Universidad</strong>: Alguien que imprima y ponga carteles o informe al alumnado.</li>
      <li>Si teneis un <strong>grupo de usuario/a</strong> poneros en contacto con nosotros. Necesitamos la colaboración de los grupos locales.</li>
      <li>Si eres <strong>docente</strong> puedes animar a tu alumnado a participar en el Concurso, llevar PFC libres o involucrarte en el jurado de esta edición.</li>

      <li>Si estás interesado en <strong>colaborar</strong> escríbenos a contacto ARROBA concursosoftwarelibre.org.</li>
      <li>Si estás interesado en <strong>participar en la organización</strong> del Concurso Universitario de Software Libre, puedes darte de alta en la lista de organizadores y realizar una colaboración acorde con tus posibilidades.</li>
      <li>Si se te da bien el diseño gráfico, puedes colaborar con nosotros en la elaboración de <strong>diseños, banners y cartelería</strong>, para ello tenemos liberado nuestro <a href="/1011/graficos">material gráfico</a> para que lo uses libremente.</li>
    </ul>

    <h2>Empresas</h2>

    <p>
      ¿Interesado en que su empresa colabore en un evento de esta magnitud? Tenemos un programa de patrocinio
      que puede solicitar a través de <strong>contacto</strong> ARROBA  <strong>concursosoftwarelibre.org</strong>.
      No dejes pasar esta oportunidad.
    </p>

    <h2>Grupos de Usuarios de Software Libre</h2>

    <p>
      ¿Quieres colaborar desde tu grupo de usuarios? Elegid una persona de contacto y envíadnos un email a
      <strong>contacto</strong> ARROBA <strong>concursosoftwarelibre.org</strong>.
    </p>

    <p>
      También disponemos de <a href ="http://concursosoftwarelibre.org/1011/colabora">recursos gráficos</a>.
    </p>
