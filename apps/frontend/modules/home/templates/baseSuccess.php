            <div class="content"><h3 id="Descripciongeneral">Descripción general.</h3>
<p>
<strong>1.</strong> El Concurso Universitario de Software Libre (en adelante CUSL o concurso)
es un concurso de desarrollo de software, hardware y documentación técnica libre
 que consiste en la elaboración y presentación de un proyecto de Software Libre desarrollado
 íntegramente con una implementación libre de cualquier lenguaje de programación.
 Los/las participantes y sus proyectos deberán cumplir los requisitos que se contemplan en los siguientes apartados.
</p>
<p>
<strong>2.</strong> El desarrollo del CUSL consta de las siguientes fases:

</p>
<ul>
<li><strong>Fase de inscripción:</strong> Durante este periodo,
  los/as participantes se inscribirán en el concurso mediante el formulario de la web.
</li>
<li><strong>Fase de aceptación:</strong>
  Se comunicará a todos los/as participantes la lista de proyectos aceptados.
</li>
<li><strong>Fase de desarrollo:</strong>
  Periodo destinado al desarrollo de los proyectos aceptados.
</li>
<li><strong>Fase final:</strong>
  Consistirá en un ciclo de charlas y talleres donde serán expuestos los proyectos finalistas y se procederá
  a la entrega de premios.
</li>
</ul>
