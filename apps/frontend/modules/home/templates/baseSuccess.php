<h3 id="Requisitosdelosparticipantesgenerales">Requisitos de los participantes: Generales.</h3>
<p>
<strong>3.</strong> El máximo de participantes será de tres personas por proyecto.
</p>

<p>
<strong>4.</strong> Sólo se podrá participar en un proyecto a la vez.
</p>
<p>
<strong>5.</strong> Tras la notificación de aceptación, los/as participantes deberán acreditar la documentación solicitada
  por la organización de lo contrario el proyecto propuesto quedará fuera del concurso.
</p>
<h3 id="Requisitosdelosparticipantessenior">Requisitos de los participantes: Modalidad universitario.</h3>
<p>
<strong>6.</strong> Los/as participantes han de ser mayores de edad y estar matriculados en primer, segundo o
  tercer ciclo en alguna de las Universidades españolas reconocidas por el Ministerio de Educación y Ciencias durante
  el curso 2010/2010, así como estar en disposición de poder acreditarlo.
</p>
<h3 id="Requisitostécnicosdelosproyectos">Requisitos técnicos de los proyectos</h3>
<p>
<strong>7.</strong> Los proyectos debe estar íntegramente desarrollados con la implementación en Software Libre de
  cualquier lenguaje de programación y la licencia elegida deberá estar comprendida dentro del conjunto
  de <a href="http://www.gnu.org/licenses/license-list.html"> licencias consideradas de Software Libre</a>
  por el proyecto GNU (<a href="http://www.gnu.org/licenses/license-list.es.html">lista en castellano no actualizada</a>).
  Las librerías utilizadas también deberán estar bajo este conjunto de licencias.</p></p>
<p>
<strong>8.</strong> La organización pondrá a disposición de los participantes una
  forja en la que se alojarán los proyecto participantes en el CUSL.
  Los participantes también dispondrán de un blog, en el que quedará reflejado la evolución del proyecto,
  las dificultades encontradas y las decisiones tomadas durante la fase de desarrollo.<br />
  Tanto la forja como el blog serán empleados para evaluar la evolución de los proyectos.
</p>
<p>
<strong>9.</strong> Para facilitar la evaluación y las labores de organización,
  todos los proyectos estarán alojados en la <a href="https://forja.rediris.es/">Forja de Conocimiento Libre de la
  Comunidad RedIRIS</a>.
</p>
<h3 id="Criteriosdeevaluación">Criterios de evaluación</h3>
<p><strong>10.</strong> El Comité de evaluación estará compuesto por un grupo de personas adjuntas a la Universidad,
  Empresas, Instituciones y la Comunidad del Software Libre que evaluarán los proyectos en base a los criterios
  descritos en el punto 13.
</p>
<p><strong>11.</strong> El comité de evaluación descrito en el punto 11, sólo valorará<br />
  aquellos proyectos y/o contribuciones realizados durante la fase de<br />
  desarrollo del CUSL, entendiendo como tal desde el día en que solicita<br />
  su inscripción hasta el día en que concluye la fase de desarrollo.</p>
<p><strong>12. </strong> Si un/a participante realiza una contribución a un proyecto
  ya existente, sólo será evaluada la parte desarrollada durante el periodo de desarrollo del CUSL,
  y en ningún momento el proyecto en global.</p>
<p><strong>13.</strong> El comité de evaluación valorará los siguientes aspectos:</p>

<ul>
<li><strong>Impacto del proyecto en la comunidad del software libre y la sociedad</strong> (30% de la puntuación).
  Se valorará positivamente que el proyecto genere<br />
  un tejido social (comunidad) con un interés común en hacer que dicho<br />
  proyecto avance. Ésto comprende aspectos como la facilidad para realizar<br />
  trabajos relacionados y/o derivados, su impacto en la esfera<br />
  socio-política y las estrategias de difusión del proyecto en la<br />
  comunidad del software libre.</li>
<li><strong>Calidad del desarrollo y uso de la forja</strong> (30% de la puntuación). Se<br />

  valorará positivamente una buena planificación, así como la realización<br />
  de un buen diseño e implementación del proyecto. Una buena estrategia en<br />
  el lanzamiento de versiones será igualmente bien considerada. Además, un<br />
  buen uso de las herramientas disponibles en la forja, tales como las<br />
  listas de correo y el sistema de gestión de código (subversion), serán<br />
  igualmente valorados positivamente.</li>
  <li><strong>Documentación del proyecto</strong> (25% de la puntuación): La documentación<br />
  es un aspecto importante. Se valorará positivamente la realización de<br />

  documentación de usuario final (detallando la instalación y<br />
  configuración) y de desarrollo (ofreciendo la información pertinente<br />
  para facilitar la realización de trabajos derivados y adaptaciones).</li>
<li><strong>Grado de finalización</strong> (15% de la puntuación): Se valorará<br />
  positivamente, aunque con menor valor que otros puntos anteriormente<br />
  descritos, el grado de finalización y usabilidad del proyecto.</li>
</ul>
</p>
