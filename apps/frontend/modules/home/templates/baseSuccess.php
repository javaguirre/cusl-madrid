<li>
  <strong>Mejor proyecto de Software Libre de la Comunidad de Madrid</strong>
</li>
<li>
  <strong>Finalista mejor proyecto de Software Libre de la Comunidad de Madrid</strong>
</li>
<p>
</ul>
<p>
<strong>15.</strong> Durante la fase final se harán público y serán entregados los premios.
  Está por confirmar el tipo de premio que se otorgará.
</p>
<ul>
</ul>
<p>

</p>
<p>
<strong>16. </strong>La organización de reserva el derecho de revocar cualquiera de los premios en caso de
  detectar alguna irregularidad. Así mismo, se reserva el derecho de anular o declarar desierto alguno de los premios
  en caso de que el comité de evaluación, antes mencionado, considere que los proyectos de alguna de las categorías no
  reúnan la calidad deseada.
</p>
<p><strong>17.</strong> Un/a representante de cada proyecto finalista deberá participar y estar presente en la fase final.
</p>
<h3>Descalificaciones.</h3>

<p>
<strong>18.</strong> La organización se reserva el derecho a tomar las medidas oportunas y/o a descalificar,
  previo aviso, aquellos participantes y/o proyectos que incumplan con algún punto de las bases durante el
  transcurso del concurso.
</p>
<h3>Aspectos legales.</h3>
<p>
<strong>19.</strong> La organización se reserva el derecho de modificar las bases del concurso, parcial o
  totalmente en cualquier momento previo aviso.
</p>
<p>
<strong>20.</strong> El hecho de presentar la inscripción al concurso, implica la aceptación de estas bases en su
  totalidad.</div>
